#include <math.h>

const double twopi = 2*M_PI;

double mod2pi( x )
double x;
{
if ( x == 0.0 ) return x;
while ( x < 0.0 ) x+= twopi;
while ( x >= twopi ) x -= twopi;
return x;
}
